const jssha = require('jssha');

const { SALT } = process.env;

const numCols = 40;
const numRows = 60;

function genGridArray(fill = null) {
  // TODO: fine tune sizing, size for tablet
  // nested arrays easier for checking adjacency vs using flat array
  const arr = [...Array(numRows)].map(() => Array(numCols).fill(fill));
  return arr;
}

function WorldState() {
  this.board = genGridArray();
  this.wallCells = [];
  this.roomCells = [];
  this.activeObjCells = [];
}

function getHash(input) {
  // eslint-disable-next-line new-cap
  const shaObj = new jssha('SHA-512', 'TEXT', { encoding: 'UTF8' });
  const unhasedString = `${input}-${SALT}`;
  shaObj.update(unhasedString);

  return shaObj.getHash('HEX');
}

